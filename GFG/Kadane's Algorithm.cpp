class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    int maxSubarraySum(int arr[], int n){
        int maxGlobal =  arr[0];
        int maxCurrent = arr[0];
        for(int i=1;i<n;i++){
            maxCurrent = max(arr[i],maxCurrent+arr[i]);
            if(maxCurrent>maxGlobal){
                maxGlobal = maxCurrent;
            }
        }
        return maxGlobal;
    }
};

